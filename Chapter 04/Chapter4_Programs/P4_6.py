numbers = [10, 20, 30, 40, 50, 60, 70, 80]
x = numbers.pop(4)  # removes the element at index 4
print(x)
print(numbers)

y = numbers.pop(0)  # removes the first element
print(y)
print(numbers)

z = numbers.pop()  # removes the last element
print(z)
print(numbers)

